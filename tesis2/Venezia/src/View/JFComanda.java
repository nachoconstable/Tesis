    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        btnaceptar.setVisible(false);
        btncancelar.setVisible(false);
        PrinterJob printerJob = PrinterJob.getPrinterJob();
        printerJob.setPrintable(this);
        
        if(printerJob.printDialog()){
            try{
                printerJob.print();
            }catch(PrinterException ex){
                System.out.println("error"+ex);
            }
        }
        try {
            g.AgregarVenta(new Ventas(Integer.parseInt(txtcodigo.getText()),txtfecha.getText()));
            int idVenta = g.UltimaVenta();
            for (int i = 0; i < tablaventas.getRowCount(); i++) {
                DtoDetalleVenta detalle = new DtoDetalleVenta ();
                detalle.setCantidad(Integer.parseInt(tablaventas.getValueAt(i, 2).toString()));
                detalle.setPrecio(Integer.parseInt(tablaventas.getValueAt(i, 3).toString()));
                detalle.setCod_producto(Integer.parseInt( tablaventas.getValueAt(i, 0).toString()));
                detalle.setId_venta(idVenta);

               g.AgregarDetalleVenta(detalle);
            } 
        } catch (SQLException ex) {
            Logger.getLogger(JFComanda.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_btnaceptarActionPerformed

