public void cargarTablaCarrito() {
    for (int i = 0; i < tablaventas.getRowCount(); i++){
        Object[] registro = new Object[3];
        registro[0] = (tablaventas.getValueAt(i, 1).toString());
        registro[1] = Integer.parseInt(tablaventas.getValueAt(i, 2).toString());
        registro[2] = Integer.parseInt(tablaventas.getValueAt(i, 3).toString());
        
        DefaultTableModel mod = new DefaultTableModel();
        mod = (DefaultTableModel) (tablacomanda.getModel());
        mod.addRow(registro);
        tablacomanda.setModel(mod);
    }
}
public int SumarColumna(){
    int suma = 0;
        for (int i = 0; i < tablaventas.getRowCount(); i++){
            suma += (Integer.parseInt(tablaventas.getValueAt(i, 2).toString())*Integer.parseInt(tablaventas.getValueAt(i, 3).toString()));
        }
        return suma;
}

