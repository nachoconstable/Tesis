    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
//"Codigo Producto","Producto", "Precio compra", "Cantidad"
        try {
            g.AgregarPedido(new Pedidos(txtfecha.getText(),txtdireccion.getText()));
            int idPedido = g.UltimoPedido();
            for (int i = 0; i < tablaCarrito.getRowCount(); i++) {
                DtoPedidoDetalle detalle = new DtoPedidoDetalle();
                detalle.setId_producto((int) tablaCarrito.getValueAt(i, 0));
                detalle.setPrecio_unitario((int)tablaCarrito.getValueAt(i, 2));
                detalle.setCantidad(Integer.parseInt(tablaCarrito.getValueAt(i, 3).toString()));
                detalle.setId_pedido(idPedido);

                g.AgregarDetallePedido(detalle);
            }
            

        } catch (Exception ex) {
            System.out.println("ERROR: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnguardarActionPerformed

