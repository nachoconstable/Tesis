 private void CargarTabla() throws SQLException {
        DefaultTableModel model = new DefaultTableModel();
        String[] nombresColumnas = new String[4];
        nombresColumnas[0] = "Cod";
        nombresColumnas[1] = "Descripcion";
        nombresColumnas[2] = "Cant";
        nombresColumnas[3] = "Precio";
        model.setColumnIdentifiers(nombresColumnas);
            Object[] registro = new Object[4];
            registro[0] = txtcod.getText();
            registro[1] = txtproducto.getText();
            registro[2] = txtcantidad.getText();
            registro[3] = txtprec.getText();
           
        DefaultTableModel mod = new DefaultTableModel();
        mod = (DefaultTableModel) (tablaventas.getModel());
        mod.addRow(registro);
        tablaventas.setModel(mod);
        }

