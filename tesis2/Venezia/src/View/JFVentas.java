public int SumarColumna(){
    int suma = 0;
        for (int i = 0; i < tablaventas.getRowCount(); i++){
           
            suma += (Integer.parseInt(tablaventas.getValueAt(i, 2).toString())*Integer.parseInt(tablaventas.getValueAt(i, 3).toString()));
            
        }
        return suma;
}
